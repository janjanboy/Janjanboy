package Pangmalakasan;

public class Exercise2 {
    public static void main(String[] args) {

        int number = 3;
        System.out.println(number); // output is 3

        number -= 1;
        System.out.println(number); // output is 2

        number *= 2;
        System.out.println(number); // output is 4

        number /= 2;
        System.out.println(number); // output is 2

        number += 8;
        System.out.println(number); // output is 10
    }
}
